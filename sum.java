public class sum {
    public static void main(String args[]){
        int A = 10;
        int B = 20;
        int sum = A + B;
        int sub = A - B;    
        int mul = A * B;
        int div = A / B;
        System.out.println("Sum: " + sum);
        System.out.println("Sub: " + sub);
        System.out.println("Mul: " + mul);
        System.out.println("Div: " + div);
    }
}
